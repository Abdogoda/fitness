function updateTheme() {
  theme = localStorage.getItem("selectedTheme") || "light";
  if (theme === "dark") {
    document.body.classList.add("dark-theme");
    document.getElementById("themeToggle").textContent = "🌙";
    document.getElementById("themeToggle").classList.add("dark");
  } else {
    document.body.classList.remove("dark-theme");
    document.getElementById("themeToggle").textContent = "☀️";
    document.getElementById("themeToggle").classList.remove("dark");
  }
}

function updateLanguage() {
  lang = localStorage.getItem("selectedLanguage") || "en";
  Object.keys(translations[lang]).forEach((key) => {
    const element = document.getElementById(key);
    if (element) {
      element.textContent = translations[lang][key];
    }
  });

  document.body.dir = lang === "ar" ? "rtl" : "ltr";
  document.getElementById("langToggle").textContent = lang.toUpperCase();
}

function displayDateTime() {
  const now = new Date();
  document.getElementById("dateTime").textContent = now.toLocaleString();
}

function toggleLanguage() {
  lang = localStorage.getItem("selectedLanguage") === "en" ? "ar" : "en";
  localStorage.setItem("selectedLanguage", lang);
  document.getElementById("langToggle").textContent = lang.toUpperCase();
  
  updateLanguage();
}

function toggleTheme() {
  theme = localStorage.getItem("selectedTheme") === "light" ? "dark" : "light";
  localStorage.setItem("selectedTheme", theme);

  updateTheme();
}

// Initialize
updateLanguage();
updateTheme();
displayDateTime();
setInterval(displayDateTime, 1000);